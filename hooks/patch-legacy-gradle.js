#!/usr/bin/env node
/*
 * Cordova `before_build` hook.
 * Patches third-party plugin .gradle files so they build against
 * Gradle 7+ / AGP 8+ / Kotlin 1.8+:
 *
 *   compile  '...'           -> implementation '...'
 *   testCompile '...'        -> testImplementation '...'
 *   androidTestCompile '...' -> androidTestImplementation '...'
 *   provided '...'           -> compileOnly '...'
 *   apk '...'                -> runtimeOnly '...'
 *   jcenter()                -> mavenCentral()
 *   altbeacon 2.16.1         -> 2.20.6 (2.16 was never on Maven Central)
 *
 * Then writes platforms/android/app/build-extras.gradle (auto-loaded by
 * cordova-android) to force a single kotlin-stdlib version. Without this,
 * a plugin pulling in kotlin-stdlib-jdk8:1.6 + AGP pulling kotlin-stdlib:1.8
 * fails the build with "Duplicate class kotlin.collections.jdk8.* found in
 * modules kotlin-stdlib-1.8.0.jar and kotlin-stdlib-jdk8-1.6.0.jar"
 * because Kotlin 1.8 inlined the JDK8 classes into the main stdlib.
 */
const fs = require('fs');
const path = require('path');

const BUILD_EXTRAS_MARKER = '// auto-generated by hooks/patch-legacy-gradle.js — do not edit';
const BUILD_EXTRAS = `${BUILD_EXTRAS_MARKER}
configurations.all {
    resolutionStrategy.eachDependency { details ->
        def req = details.requested
        if (req.group == 'org.jetbrains.kotlin' && req.name.startsWith('kotlin-stdlib')) {
            details.useVersion '1.9.24'
        }
    }
}
`;

module.exports = function (ctx) {
    const rootDir = ctx && ctx.opts && ctx.opts.projectRoot
        ? ctx.opts.projectRoot
        : path.resolve(__dirname, '..');
    const androidDir = path.join(rootDir, 'platforms', 'android');
    if (!fs.existsSync(androidDir)) return;

    const subs = [
        [/^(\s*)compile(\s+["'])/gm, '$1implementation$2'],
        [/^(\s*)testCompile(\s+["'])/gm, '$1testImplementation$2'],
        [/^(\s*)androidTestCompile(\s+["'])/gm, '$1androidTestImplementation$2'],
        [/^(\s*)provided(\s+["'])/gm, '$1compileOnly$2'],
        [/^(\s*)apk(\s+["'])/gm, '$1runtimeOnly$2'],
        [/\bjcenter\s*\(\s*\)/g, 'mavenCentral()'],
        [/org\.altbeacon:android-beacon-library:2\.16\.1\b/g, 'org.altbeacon:android-beacon-library:2.20.6']
    ];

    const visit = (dir) => {
        for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
            const full = path.join(dir, entry.name);
            if (entry.isDirectory()) {
                // Skip Cordova framework + build output to limit blast radius.
                if (entry.name === 'app' || entry.name === 'CordovaLib' || entry.name === 'build' || entry.name === '.gradle') continue;
                visit(full);
            } else if (entry.name.endsWith('.gradle')) {
                let src = fs.readFileSync(full, 'utf8');
                let changed = false;
                for (const [re, rep] of subs) {
                    const next = src.replace(re, rep);
                    if (next !== src) changed = true;
                    src = next;
                }
                if (changed) {
                    fs.writeFileSync(full, src);
                    console.log('[patch-legacy-gradle] updated', path.relative(rootDir, full));
                }
            }
        }
    };

    visit(androidDir);

    // Write build-extras.gradle (cordova-android auto-applies it to :app).
    const appDir = path.join(androidDir, 'app');
    if (fs.existsSync(appDir)) {
        const extras = path.join(appDir, 'build-extras.gradle');
        let existing = '';
        try { existing = fs.readFileSync(extras, 'utf8'); } catch (_) {}
        if (existing !== BUILD_EXTRAS) {
            fs.writeFileSync(extras, BUILD_EXTRAS);
            console.log('[patch-legacy-gradle] wrote', path.relative(rootDir, extras));
        }
    }
};

if (require.main === module) {
    module.exports({ opts: { projectRoot: path.resolve(__dirname, '..') } });
}

